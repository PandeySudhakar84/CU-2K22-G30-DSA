package Lec5;

public class Assignment_Operator {
    public static void main(String[] args) {
        int a=3;
       // a=a+5;
        a+=5; // a=a+5
        System.out.println(a);
        a *=5; // a=a*5
        System.out.println(a);
        a/=2; // a=a/2
        System.out.println(a);
    }
}
