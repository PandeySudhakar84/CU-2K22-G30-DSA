package Lec5;
// Operator is a spacial symbol which is use to perform logical or mathemetical operation on data or variable
// operand is a data or variable on which operation is to be perform
public class Arithmetic_Opetator {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        int c=a+b;
        System.out.println(c);
        c=b-a;
        System.out.println(c);
        c=a*b;
        System.out.println(c);
        c=b/a;
        System.out.println();
        c=b%a;
        System.out.println(c);
    }
}
